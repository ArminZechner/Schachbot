void Display::printInformationBoard(){
    displayMode = DISPLAY_MODE::infoBoard;
    if(gameMode == GAMEMODE::roboter){
    tft.fillScreen(TFT_GREEN);
    }
    else{
    tft.fillScreen(TFT_SKYBLUE);
    }
    tft.setTextColor(TFT_WHITE);
    tft.setTextSize(10);

    tft.drawChar(selectedFields[0].x, 100, 40);
    tft.drawChar(selectedFields[0].y, 180, 40);
    tft.drawChar(selectedFields[1].x, 260, 40);
    tft.drawChar(selectedFields[1].y, 340, 40);

    tft.fillRect(100, 95, 38, 10, TFT_WHITE);
    tft.fillRect(180, 95, 38, 10, TFT_WHITE);
    tft.fillRect(260, 95, 38, 10, TFT_WHITE);
    tft.fillRect(340, 95, 38, 10, TFT_WHITE);

    tft.setCursor(10,130);
    tft.setTextSize(4);
    tft.print(message);
}
int Display::getWhiteOrBlack(int figur) {
    if(figur == e_figuren::whitePawnQueen) {
        return Color::white;
    }
    else if(figur == e_figuren::blackPawnQueen) {
        return Color::black;
    }
    if (figur >= e_figuren::whitePawn &&
        figur <= e_figuren::whiteKing) {
        return Color::white;
    }
    else {
        return Color::black;
    }
}
int getPieceWithOutColor(int p) {
    if (p == e_figuren::empty) {
        return empty;
    }
    // Ein umgewandelter Bauer wird als Dame dargestellt (Koenigin-Bitmap).
    // Die richtige Farbe kommt weiterhin aus getWhiteOrBlack().
    if (p == e_figuren::whitePawnQueen ||
        p == e_figuren::blackPawnQueen) {
        return e_figuren::whiteQueen;
    }
    if (p >= e_figuren::whitePawn &&
        p <= e_figuren::whiteKing) {
        return p;
    }
    else {
        return p - 6;
    }
}
void Display::printChessfield(){
    displayMode = DISPLAY_MODE::chessBoard;
    bool schachMuster = false;

    for(int y = 0; y < 8; y++){
        for(int x = 0; x < 2; x++){
            schachMuster ? tft.fillRect(FIELD_SIZE_X * x, FIELD_SIZE_Y * y, FIELD_SIZE_X, FIELD_SIZE_Y, tft.color565(160, 190, 220)) :
            tft.fillRect(FIELD_SIZE_X * x, FIELD_SIZE_Y * y, FIELD_SIZE_X, FIELD_SIZE_Y, tft.color565(220, 235, 250));
            
            schachMuster ? tft.fillRect(FIELD_SIZE_X * (x + 10), FIELD_SIZE_Y * y, FIELD_SIZE_X, FIELD_SIZE_Y, tft.color565(190, 150, 130)) :
            tft.fillRect(FIELD_SIZE_X * (x + 10), FIELD_SIZE_Y * y, FIELD_SIZE_X, FIELD_SIZE_Y, tft.color565(240, 220, 200));
            if(soll_schachbrettFriedhof[0][x][y] != e_figuren::empty){
            tft.drawBitmap(x*FIELD_SIZE_X, y*FIELD_SIZE_Y, epd_bitmap_allArray[getPieceWithOutColor(soll_schachbrettFriedhof[0][x][y])], 40, 40, getWhiteOrBlack(soll_schachbrettFriedhof[0][x][y]) ? TFT_BLACK : TFT_WHITE);
            }
            if(soll_schachbrettFriedhof[1][x][y] != e_figuren::empty){
            tft.drawBitmap((x+10)*FIELD_SIZE_X, y*FIELD_SIZE_Y, epd_bitmap_allArray[getPieceWithOutColor(soll_schachbrettFriedhof[1][x][y])], 40, 40, getWhiteOrBlack(soll_schachbrettFriedhof[1][x][y]) ? TFT_BLACK : TFT_WHITE);
            }
            schachMuster = !schachMuster;
        }
        schachMuster = !schachMuster;
    }
    for(int y = 0; y < 8; y++){
        for(int x = 2; x < 10; x++){
            schachMuster ? tft.fillRect(FIELD_SIZE_X * x, FIELD_SIZE_Y * y, FIELD_SIZE_X, FIELD_SIZE_Y, tft.color565(115, 149, 82)) :
            tft.fillRect(FIELD_SIZE_X * x, FIELD_SIZE_Y * y, FIELD_SIZE_X, FIELD_SIZE_Y, tft.color565(235, 236, 208));
            if(soll_schachbrett[x-2][y] != e_figuren::empty){
            tft.drawBitmap(x*FIELD_SIZE_X, y*FIELD_SIZE_Y, epd_bitmap_allArray[getPieceWithOutColor(soll_schachbrett[x-2][y])], 40, 40, getWhiteOrBlack(soll_schachbrett[x-2][y]) ? TFT_BLACK : TFT_WHITE);
            }
            schachMuster = !schachMuster;
        }
        schachMuster = !schachMuster;
    }
}